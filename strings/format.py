a = 15
b = 20 
print('{} + {} = {}'.format(a,b,a+b))
print('{} - {} = {}'.format(a,b,a-b))
print('{} * {} = {}'.format(a,b,a*b))
print('{} / {} = {}'.format(a,b,a/b))
print('{} % {} = {}'.format(a,b,a%b))

# 15 + 20 = 35
# 15 - 20 = -5
# 15 * 20 = 300
# 15 / 20 = 0.75
# 15 % 20 = 15
